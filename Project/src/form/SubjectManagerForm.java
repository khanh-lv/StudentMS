    private void loadTable(List<Subject> subjectList) {
        DefaultTableModel model = (DefaultTableModel) tblSub.getModel();
        model.setRowCount(0);
        int count = 1;
        for (Subject s : subjectList) {
            if (s.getStatus() == 1) {
                model.addRow(new Object[]{
                    count, s.getSubjectNo(), s.getSubjectName()
                });
                count++;

            }
        }
    }

